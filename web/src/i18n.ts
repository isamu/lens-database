export type Locale = "ja" | "en";

export const isLocale = (value: unknown): value is Locale => value === "ja" || value === "en";

export const browserLocale = (): Locale =>
  String(navigator.language || "")
    .toLowerCase()
    .startsWith("ja")
    ? "ja"
    : "en";

export const i18n = createI18n({
  legacy: false,
  locale: "en",
  fallbackLocale: "en",
  messages: { en, ja },
});
