// Konica AR-mount Hexanon manual-focus lens catalog (1965-1988).
//
// The Konica AR mount was introduced with the Konica Auto-Reflex (1965) and
// used through the FS-1 / FT-1 era. Konica withdrew from the interchangeable-
// lens SLR market in 1988 to concentrate on point-and-shoot cameras, so every
// AR-mount Hexanon is `discontinued: true` with `discontinuedDate: "1988"`.
//
// Generations, in rough chronological order:
//   * Early "Hexanon" (non-EE): pre-1972, no automatic-exposure coupling.
//   * "Hexanon AR" / EE: 1972-onwards, EE-coupling tab for the Autoreflex T3.
//   * Late "Hexanon AR" ("T-mark" / new-shell): mid-1970s onwards, updated
//     cosmetics; the FS-1 (1979) and FT-1 (1983) accept these as well as EE
//     lenses. There is no autofocus AR lens.
//
// Konica did not assign JAN/EAN codes to AR lenses (predates the JAN system
// for lenses), so IDs use descriptive slugs. No fabricated 13-digit codes.
//
// `officialUrl` and `urls` are intentionally omitted: neither Konica Minolta
// nor Sony (successor) maintain product pages for AR-mount Hexanon lenses.
//
