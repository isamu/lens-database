//   * Hexanon AR 40mm f/1.8 — compact pancake, praised for sharpness/size.
//   * Hexanon AR 50mm f/1.4 — the "smooth Hexanon", a reference-quality
//     standard prime widely adapted to Sony E / Fujifilm X mirrorless.
//   * Hexanon AR 57mm f/1.2 — extreme aperture, rare, collector piece.
