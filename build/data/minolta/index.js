// (MD, 1977). All MC lenses mount and stop down on MD bodies and vice versa,
// so we register this whole family under `mount: "MD"` per the schema.
//
// Generations:
//   * MC I / MC II / MC-X  (1966-1977) — "MC Rokkor" / "MC Rokkor-X" branding
//   * MD I  (1977-1978)    — first-gen MD, still metal focus ring
//   * MD II (1978-1981)    — rubber focus ring, mostly Rokkor / Rokkor-X badges
//   * MD III (1981-1990)   — "New MD" without Rokkor name, plastic barrel,
//                            lighter and smaller. Killed in 1985-1990 as the
