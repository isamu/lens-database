// Canon Serenar / Canon Lens LTM (Leica Thread Mount / L39 / M39x1 screw)
// rangefinder lenses. Canon Camera Co., 1939-1970.
//
// Canon's rangefinder line began in 1939 with the Hansa Canon and matured
// through the S / II / IV / P / VI-L bodies and culminated with the Canon 7
// (1961) and 7s / 7sZ (1965-1968), before Canon exited the rangefinder
// market in 1970 to focus on the FL / FD SLR system. Early lenses (through
// ~1953) were sold under the "Serenar" brand; later production dropped the
// Serenar name and used "Canon Lens" branding, though the optical designs
// often continued largely unchanged.
//
